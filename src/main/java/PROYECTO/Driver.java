    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int min = (Integer)jSpinner1.getValue();
        int max = (Integer)jSpinner2.getValue();
        if (max<min){
            max = min;
            jSpinner2.setValue(min);
        }
        for (int i=0; i<grafoKruskal.A; i++){
            grafoKruskal.aristas[i].peso = grafoPrim.aristas[i].peso = rand.nextInt(min,max);
        }
        grafoKruskal.repaint();
        grafoPrim.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int graphType = jComboBox1.getSelectedIndex()+1;
        String nombre = graphType +"-"+ System.currentTimeMillis() +".json";
        org.openjdk.jmh.runner.options.Options opt = new OptionsBuilder()
                .include(Kruskal.class.getSimpleName())
                .include(Prim.class.getSimpleName())
                .resultFormat(ResultFormatType.JSON)
                .result(nombre)
                .mode(Mode.Throughput)
                .warmupIterations(3)
                .param("p", grafoKruskal.obtenerParametros())
                .forks(2)
                .timeUnit(TimeUnit.SECONDS)
                .build();
                //.measurementIterations(1)
                //.shouldDoGC(true)
                //.addProfiler(StackProfiler.class)
                //.jvmArgsAppend("-Djmh.stack.period=1")
        try {
            new Runner(opt).run();
            leerResultado(nombre);
        } catch (RunnerException ex) {
            Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

