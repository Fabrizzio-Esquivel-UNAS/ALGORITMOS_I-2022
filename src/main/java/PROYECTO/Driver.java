    private void agregarResultado(JSONObject res, String grafo){
        JSONObject pm = (JSONObject)res.get("primaryMetric");
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{
            (String)res.get("benchmark"), 
            (String)res.get("mode"),
            ((Double)pm.get("score")).toString(),
            grafo
        });
    }
    
    private void leerResultado(String name){
        JSONParser jsonParser = new JSONParser();
        try (FileReader reader = new FileReader(name)){
            Object obj = jsonParser.parse(reader);
            JSONArray list = (JSONArray)obj;
            list.forEach(res -> agregarResultado((JSONObject)res, name.substring(0,1)));
        } catch (FileNotFoundException e) {
        } catch (IOException | ParseException ex) {
            Logger.getLogger(Driver.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void buscarResultados(){        
        File dir = new File(Paths.get("").toAbsolutePath().toString());
        File[] dirListing = dir.listFiles();
        if (dirListing != null) {
            for (File f : dirListing){
                //Obtener la extension del archivo
                String name = f.getName();
                String ext="";
                int i = name.lastIndexOf('.');
                if (i>=0)
                    ext = name.substring(i+1);
                //Comprobar la extension
                if ("json".equals(ext)){
                    leerResultado(name);
                    //System.out.println("ARCHIVO: "+f.getAbsolutePath());
                }
            }
        }else{
            System.out.println("DIRECTORIO NO VALIDO");
        }        
    }
    
