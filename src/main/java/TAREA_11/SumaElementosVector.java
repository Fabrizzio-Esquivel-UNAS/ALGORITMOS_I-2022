    ArrayList<Integer> datos = new ArrayList<>();
    int recursivo(int i, int x){
        if (i==0){
            return x;
        }else{
            return recursivo(--i, x+datos.get(i));
        }
    }
    int iterativo(int i, int x){
        while (i!=0){
            x += datos.get(--i);
        }
        return x;
    }
    
